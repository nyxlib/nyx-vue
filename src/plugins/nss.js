/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

let connected = false;

let endpoint = '';

/*--------------------------------------------------------------------------------------------------------------------*/
/* FUNCTIONS                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const _connected_func = () => connected;

const _endpoint_func = () => endpoint;

/*--------------------------------------------------------------------------------------------------------------------*/

const _update_func = (_endpoint) => {

    endpoint = (_endpoint || '').trim();
};

/*--------------------------------------------------------------------------------------------------------------------*/

export default {

    install(app)
    {
        app.provide('nss', {
            /* INITIALISATION */
            connected: _connected_func,
            endpoint: _endpoint_func,
            update: _update_func,
        });
    }
};

