<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import {ref, provide, nextTick, computed} from 'vue';

import {Tab} from 'bootstrap';

/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

defineProps({
    card: {
        type: Boolean,
        default: false,
    },
    navClass: {
        type: [String, Array, Object],
        default: 'mb-3',
    },
    contentClass: {
        type: [String, Array, Object],
        default: '',
    },
});

/*--------------------------------------------------------------------------------------------------------------------*/

const tabs = ref({});

const tabListRef = ref(null);

/*--------------------------------------------------------------------------------------------------------------------*/

const sortedTabs = computed(() => Object.values(tabs.value).sort((x, y) => x.tabRank - y.tabRank));

/*--------------------------------------------------------------------------------------------------------------------*/
/* FUNCTIONS                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

provide('addTab', /* NOSONAR */ (tabId, tabRank, tabTitle, tabIcon, tabClass, onShow, onShown, onHide, onHidden) => {

    const tab = tabs.value[tabId] = {
        tabId: tabId,
        tabRank: tabRank,
        tabTitle: tabTitle,
        tabIcon: tabIcon,
        tabClass: tabClass,
        /**/
        tabEl: null,
        tabInstance: null,
        /**/
        onShow: onShow,
        onShown: onShown,
        onHide: onHide,
        onHidden: onHidden,
    };

    nextTick(() => {

        /*------------------------------------------------------------------------------------------------------------*/

        const el = tabListRef.value?.querySelector(`button[data-bs-target="#${tabId}"]`);

        /*------------------------------------------------------------------------------------------------------------*/

        if(el)
        {
            /*--------------------------------------------------------------------------------------------------------*/

            const instance = Tab.getOrCreateInstance(el);

            /*--------------------------------------------------------------------------------------------------------*/

            tab.tabEl = el;

            tab.tabInstance = instance;

            /*--------------------------------------------------------------------------------------------------------*/

            if(onShow) {
                el.addEventListener('show.bs.tab', onShow);
            }

            if(onShown) {
                el.addEventListener('shown.bs.tab', onShown);
            }

            if(onHide) {
                el.addEventListener('hide.bs.tab', onHide);
            }

            if(onHidden) {
                el.addEventListener('hidden.bs.tab', onHidden);
            }

            /*--------------------------------------------------------------------------------------------------------*/
        }

        /*------------------------------------------------------------------------------------------------------------*/
    });

    return Object.keys(tabs.value).length === 1;
});

/*--------------------------------------------------------------------------------------------------------------------*/

provide('delTab', (tabId) => {

    /*----------------------------------------------------------------------------------------------------------------*/

    const tab = tabs.value[tabId];

    /*----------------------------------------------------------------------------------------------------------------*/

    if(tab?.tabEl)
    {
        if(tab.onShow) {
            tab.tabEl.removeEventListener('show.bs.tab', tab.onShow);
        }

        if(tab.onShown) {
            tab.tabEl.removeEventListener('shown.bs.tab', tab.onShown);
        }

        if(tab.onHide) {
            tab.tabEl.removeEventListener('hide.bs.tab', tab.onHide);
        }

        if(tab.onHidden) {
            tab.tabEl.removeEventListener('hidden.bs.tab', tab.onHidden);
        }
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    if(tab?.tabInstance)
    {
        tab.tabInstance.dispose();
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    delete tabs.value[tabId];

    /*----------------------------------------------------------------------------------------------------------------*/
});

/*--------------------------------------------------------------------------------------------------------------------*/

provide('updateTitle', (tabId, tabTitle) => {

    if(tabs.value[tabId])
    {
        tabs.value[tabId].tabTitle = tabTitle;
    }
});

/*--------------------------------------------------------------------------------------------------------------------*/

provide('updateIcon', (tabId, tabIcon) => {

    if(tabs.value[tabId])
    {
        tabs.value[tabId].tabIcon = tabIcon;
    }
});

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->
    <!-- CARD VERSION                                                                                                -->
    <!-- *********************************************************************************************************** -->

    <div class="card" v-if="card">

        <!-- ******************************************************************************************************* -->
        <!-- NAV TABS                                                                                                -->
        <!-- ******************************************************************************************************* -->

        <div class="card-header nav nav-tabs" style="padding-bottom: 1px;" role="tablist" ref="tabListRef">

            <!-- *************************************************************************************************** -->

            <button :class="['nav-link', 'px-3', 'py-2', tab.tabClass, {'active': idx === 0}]" type="button" data-bs-toggle="tab" :data-bs-target="`#${tab.tabId}`" role="tab" v-for="(tab, idx) in sortedTabs" :key="tab.tabId">

                <i :class="['bi', `bi-${tab.tabIcon}`]" v-if="tab.tabIcon"></i>

                {{ tab.tabTitle }}

            </button>

            <!-- *************************************************************************************************** -->

            <div :class="['pb-2', {'ms-auto': !sortedTabs.some((tab) => tab.tabClass?.includes('ms-auto') || tab.tabClass?.includes('me-auto'))}]">

                <slot name="button"></slot>

            </div>

            <!-- *************************************************************************************************** -->

        </div>

        <!-- ******************************************************************************************************* -->
        <!-- TABS CONTENT                                                                                            -->
        <!-- ******************************************************************************************************* -->

        <div :class="['card-body', 'tab-content', contentClass]">

            <slot></slot>

        </div>

        <!-- ******************************************************************************************************* -->

    </div>

    <!-- *********************************************************************************************************** -->
    <!-- STD VERSION                                                                                                 -->
    <!-- *********************************************************************************************************** -->

    <template v-else>

        <!-- ******************************************************************************************************* -->
        <!-- NAV TABS                                                                                                -->
        <!-- ******************************************************************************************************* -->

        <div :class="['nav', 'nav-tabs', navClass]" role="tablist" ref="tabListRef">

            <!-- *************************************************************************************************** -->

            <button :class="['nav-link', 'px-3', 'py-2', tab.tabClass, {'active': idx === 0}]" type="button" data-bs-toggle="tab" :data-bs-target="`#${tab.tabId}`" role="tab" v-for="(tab, idx) in sortedTabs" :key="tab.tabId">

                <i :class="['bi', `bi-${tab.tabIcon}`]" v-if="tab.tabIcon"></i>

                {{ tab.tabTitle }}

            </button>

            <!-- *************************************************************************************************** -->

            <div :class="['pb-2', {'ms-auto': !sortedTabs.some((tab) => tab.tabClass?.includes('ms-auto') || tab.tabClass?.includes('me-auto'))}]">

                <slot name="button"></slot>

            </div>

            <!-- *************************************************************************************************** -->

        </div>

        <!-- ******************************************************************************************************* -->
        <!-- TABS CONTENT                                                                                            -->
        <!-- ******************************************************************************************************* -->

        <div :class="['tab-content', contentClass]">

            <slot></slot>

        </div>

        <!-- ******************************************************************************************************* -->

    </template>

    <!-- *********************************************************************************************************** -->

</template>
