<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import {ref, watch, inject, onMounted, onUnmounted} from 'vue';

import * as uuid from 'uuid';

/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const addTab = inject('addTab');
const delTab = inject('delTab');

const updateTitle = inject('updateTitle');
const updateIcon = inject('updateIcon');

/*--------------------------------------------------------------------------------------------------------------------*/

const props = defineProps({
    title: {
        type: String,
        required: true,
    },
    icon: {
        type: String,
        default: '',
    },
    tabClass: {
        type: [String, Array, Object],
        default: '',
    },
    paneClass: {
        type: [String, Array, Object],
        default: '',
    },
    rank: {
        type: Number,
        default: -1,
    }
});

/*--------------------------------------------------------------------------------------------------------------------*/

const emit = defineEmits([
    'show', 'shown',
    'hide', 'hidden',
]);

/*--------------------------------------------------------------------------------------------------------------------*/

const isFirst = ref(false);

/*--------------------------------------------------------------------------------------------------------------------*/

const tabId = `nyx_tab_${uuid.v4()}`;

/*--------------------------------------------------------------------------------------------------------------------*/
/* FUNCTIONS                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const onShow = (e) => {

    emit('show', e);
};

const onShown = (e) => {

    emit('shown', e);
};

const onHide = (e) => {

    emit('hide', e);
};

const onHidden = (e) => {

    emit('hidden', e);
};

/*--------------------------------------------------------------------------------------------------------------------*/
/* WATCHERS                                                                                                           */
/*--------------------------------------------------------------------------------------------------------------------*/

watch(() => props.title, (title) => {

    if(updateTitle)
    {
        updateTitle(tabId, title);
    }

}, {immediate: false});

/*--------------------------------------------------------------------------------------------------------------------*/

watch(() => props.icon, (icon) => {

    if(updateIcon)
    {
        updateIcon(tabId, icon);
    }

}, {immediate: false});

/*--------------------------------------------------------------------------------------------------------------------*/
/* INITIALIZATION                                                                                                     */
/*--------------------------------------------------------------------------------------------------------------------*/

onMounted(() => {

    if(addTab)
    {
        isFirst.value = addTab(tabId, props.rank, props.title, props.icon, props.tabClass, onShow, onShown, onHide, onHidden);
    }
});

/*--------------------------------------------------------------------------------------------------------------------*/

onUnmounted(() => {

    if(delTab)
    {
        delTab(tabId);
    }
});

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->

    <div :class="['tab-pane', 'h-100', {'show': isFirst, 'active': isFirst}]" role="tabpanel" :id="tabId">

        <div :class="['h-100', paneClass]">

            <slot></slot>

        </div>

    </div>

    <!-- *********************************************************************************************************** -->

</template>
