<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import {inject, computed, reactive, onMounted, onUnmounted} from 'vue';

/*--------------------------------------------------------------------------------------------------------------------*/

import useNyxStore from '../stores/nyx';

import NyxHome from './dashboard/NyxHome.vue';
import NyxDevice from './dashboard/NyxDevice.vue';

import NavTabs from './ui/container/NavTabs.vue';
import TabPane from './ui/container/TabPane.vue';

/*--------------------------------------------------------------------------------------------------------------------*/
/* VARIABLES                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const props = defineProps({
    showDevices: {
        type: Boolean,
        default: true,
    },
});

/*--------------------------------------------------------------------------------------------------------------------*/

const emit = defineEmits([
    'connect',
    'disconnect',
]);

/*--------------------------------------------------------------------------------------------------------------------*/

const mqtt = inject('mqtt');
const nss = inject('nss');
const nyx = inject('nyx');

/*--------------------------------------------------------------------------------------------------------------------*/

const nyxStore = useNyxStore();

/*--------------------------------------------------------------------------------------------------------------------*/
/* FUNCTIONS                                                                                                          */
/*--------------------------------------------------------------------------------------------------------------------*/

const devices = computed(() => {

    const result = {};

    Object.values(nyxStore.defXXXVectorDict).forEach((defXXXVector) => {

        /*------------------------------------------------------------------------------------------------------------*/

        if(!('@group' in defXXXVector) || !defXXXVector['@group'])
        {
            defXXXVector['@group'] = 'Main';
        }

        /*------------------------------------------------------------------------------------------------------------*/

        let device;

        if(defXXXVector['@device'] in result) {
            device = result[defXXXVector['@device']] /**/;
        }
        else {
            device = result[defXXXVector['@device']] = {};
        }

        /*------------------------------------------------------------------------------------------------------------*/

        let group;

        if(defXXXVector['@group'] in device) {
            group = device[defXXXVector['@group']] /**/;
        }
        else {
            group = device[defXXXVector['@group']] = {};
        }

        /*------------------------------------------------------------------------------------------------------------*/

        group[defXXXVector['@name']] = defXXXVector;

        /*------------------------------------------------------------------------------------------------------------*/
    });

    return reactive(result);
});

/*--------------------------------------------------------------------------------------------------------------------*/
/* INITIALIZATION                                                                                                     */
/*--------------------------------------------------------------------------------------------------------------------*/

onMounted(() => {

    nyx.init(mqtt, nss);
});

/*--------------------------------------------------------------------------------------------------------------------*/

onUnmounted(() => {

    nyx.final();
});

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->
    <!-- DASHBOARD                                                                                                   -->
    <!-- *********************************************************************************************************** -->

    <nav-tabs margin="mb-2" content-class="flex-grow-1 overflow-y-auto">

        <!-- ******************************************************************************************************* -->
        <!-- HOME PANEL                                                                                              -->
        <!-- ******************************************************************************************************* -->

        <tab-pane pane-class="d-flex flex-column align-items-center justify-content-center" title="Home" icon="house">

            <nyx-home @connect="emit('connect')" @disconnect="emit('disconnect')">

                <slot name="home"></slot>

            </nyx-home>

        </tab-pane>

        <!-- ******************************************************************************************************* -->
        <!-- NYX PANEL                                                                                               -->
        <!-- ******************************************************************************************************* -->

        <tab-pane :pane-class="['d-flex', 'flex-column', {'align-items-center justify-content-center': Object.keys(devices).length === 0}]" title="Nyx" icon="command" v-if="showDevices">

            <!-- *************************************************************************************************** -->

            <nav-tabs margin="mb-2" v-if="Object.keys(devices).length > 0">

                <tab-pane pane-class="d-flex flex-column align-items-center justify-content-center" :title="deviceName" icon="command" v-for="(deviceDescr, deviceName) in devices" :key="deviceName">

                    <nyx-device :device-name="deviceName"
                                :device-descr="deviceDescr"
                    />

                </tab-pane>

            </nav-tabs>

            <!-- *************************************************************************************************** -->

            <div class="text-center" v-else>
                <span class="spinner-grow" style="width: 2.5rem; height: 2.5rem;"></span>
                <br />
                Waiting for connection...
            </div>

            <!-- *************************************************************************************************** -->

        </tab-pane>

        <!-- ******************************************************************************************************* -->
        <!-- MISC PANELS                                                                                             -->
        <!-- ******************************************************************************************************* -->

        <slot name="panels"></slot>

        <!-- ******************************************************************************************************* -->
        <!-- MISC BUTTONS                                                                                            -->
        <!-- ******************************************************************************************************* -->

        <template #button>

            <slot name="button"></slot>

        </template>

        <!-- ******************************************************************************************************* -->

    </nav-tabs>

    <!-- *********************************************************************************************************** -->

</template>
